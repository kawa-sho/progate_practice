package 変数を使ってみよう;

public class 自己代入 {
	public static void main(String[] args) {
	    int number = 3;
	    System.out.println(number);

	    // 変数numberの値に7を足して、変数numberを上書きしてください
	    number += 7;

	    // 変数numberを出力してください
	    System.out.println(number);

	  }
}

//変数を上書きします。
//・「+」を用いて、変数numberの値に7を足して、変数numberを上書きしてください。
//・再度、変数numberを出力してください。